def get_all_translations():
    """Return combined dict of all official HoK translations."""
    combined = {}
    combined.update(HERO_NAMES)
    combined.update(GAME_TERMS)
    combined.update(LORE_TERMS)
    return combined


def export_json(output_path=None):
    """Export all translations to a flat JSON file for the pipeline."""
    import os
    if output_path is None:
        output_path = os.path.join(
            os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
            "translations", "hok_official_terms.json"
        )
    all_trans = get_all_translations()
    sorted_trans = dict(sorted(all_trans.items()))
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(sorted_trans, f, ensure_ascii=False, indent=2)
    print(f"Exported {len(sorted_trans)} terms to: {output_path}")
    return output_path


